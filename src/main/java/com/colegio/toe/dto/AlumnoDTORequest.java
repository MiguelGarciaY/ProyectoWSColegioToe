package com.colegio.toe.dto;

public class AlumnoDTORequest {
	private Integer codigoAlumno;
	private String activo;
	private String nombreAlumno;
	private String documento;
	private Integer edadAlumno;
	private Double pesoAlumno;
	private String representante;
	private String numeroTelf;
	private String domicilio;
	private Integer faltasAlumno;
	private String conductaAlumno;
	public Integer getCodigoAlumno() {
		return codigoAlumno;
	}
	public void setCodigoAlumno(Integer codigoAlumno) {
		this.codigoAlumno = codigoAlumno;
	}
	public String getActivo() {
		return activo;
	}
	public void setActivo(String activo) {
		this.activo = activo;
	}
	public String getNombreAlumno() {
		return nombreAlumno;
	}
	public void setNombreAlumno(String nombreAlumno) {
		this.nombreAlumno = nombreAlumno;
	}
	public String getDocumento() {
		return documento;
	}
	public void setDocumento(String documento) {
		this.documento = documento;
	}
	public Integer getEdadAlumno() {
		return edadAlumno;
	}
	public void setEdadAlumno(Integer edadAlumno) {
		this.edadAlumno = edadAlumno;
	}
	public Double getPesoAlumno() {
		return pesoAlumno;
	}
	public void setPesoAlumno(Double pesoAlumno) {
		this.pesoAlumno = pesoAlumno;
	}
	public String getRepresentante() {
		return representante;
	}
	public void setRepresentante(String representante) {
		this.representante = representante;
	}
	public String getNumeroTelf() {
		return numeroTelf;
	}
	public void setNumeroTelf(String numeroTelf) {
		this.numeroTelf = numeroTelf;
	}
	public String getDomicilio() {
		return domicilio;
	}
	public void setDomicilio(String domicilio) {
		this.domicilio = domicilio;
	}
	public Integer getFaltasAlumno() {
		return faltasAlumno;
	}
	public void setFaltasAlumno(Integer faltasAlumno) {
		this.faltasAlumno = faltasAlumno;
	}
	public String getConductaAlumno() {
		return conductaAlumno;
	}
	public void setConductaAlumno(String conductaAlumno) {
		this.conductaAlumno = conductaAlumno;
	}
}
