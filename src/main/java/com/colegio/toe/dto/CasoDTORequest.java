package com.colegio.toe.dto;

public class CasoDTORequest {
	private Integer codigoCaso;
	private String detalleCaso;
	private String fechaCaso;
	private String tutorCaso;
	private String activoCaso;
	private String prioridadCaso;
	public Integer getCodigoCaso() {
		return codigoCaso;
	}
	public void setCodigoCaso(Integer codigoCaso) {
		this.codigoCaso = codigoCaso;
	}
	public String getDetalleCaso() {
		return detalleCaso;
	}
	public void setDetalleCaso(String detalleCaso) {
		this.detalleCaso = detalleCaso;
	}
	public String getFechaCaso() {
		return fechaCaso;
	}
	public void setFechaCaso(String fechaCaso) {
		this.fechaCaso = fechaCaso;
	}
	public String getTutorCaso() {
		return tutorCaso;
	}
	public void setTutorCaso(String tutorCaso) {
		this.tutorCaso = tutorCaso;
	}
	public String getActivoCaso() {
		return activoCaso;
	}
	public void setActivoCaso(String activoCaso) {
		this.activoCaso = activoCaso;
	}
	public String getPrioridadCaso() {
		return prioridadCaso;
	}
	public void setPrioridadCaso(String prioridadCaso) {
		this.prioridadCaso = prioridadCaso;
	}
}
