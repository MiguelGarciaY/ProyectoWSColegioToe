package com.colegio.toe.dto;

public class AliadoDTORequest {
	private Integer codigoAliado;
	private String detalleAliado;
	private String emailAliado;
	private String telfAliado;
	public Integer getCodigoAliado() {
		return codigoAliado;
	}
	public void setCodigoAliado(Integer codigoAliado) {
		this.codigoAliado = codigoAliado;
	}
	public String getDetalleAliado() {
		return detalleAliado;
	}
	public void setDetalleAliado(String detalleAliado) {
		this.detalleAliado = detalleAliado;
	}
	public String getEmailAliado() {
		return emailAliado;
	}
	public void setEmailAliado(String emailAliado) {
		this.emailAliado = emailAliado;
	}
	public String getTelfAliado() {
		return telfAliado;
	}
	public void setTelfAliado(String telfAliado) {
		this.telfAliado = telfAliado;
	}
}
