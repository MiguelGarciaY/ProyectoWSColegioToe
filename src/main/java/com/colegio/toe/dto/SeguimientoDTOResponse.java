package com.colegio.toe.dto;

public class SeguimientoDTOResponse {
	private Integer codigoSeguimiento;
	private String detalleSeguimiento;
	private String fechaSeguimiento;
	public Integer getCodigoSeguimiento() {
		return codigoSeguimiento;
	}
	public void setCodigoSeguimiento(Integer codigoSeguimiento) {
		this.codigoSeguimiento = codigoSeguimiento;
	}
	public String getDetalleSeguimiento() {
		return detalleSeguimiento;
	}
	public void setDetalleSeguimiento(String detalleSeguimiento) {
		this.detalleSeguimiento = detalleSeguimiento;
	}
	public String getFechaSeguimiento() {
		return fechaSeguimiento;
	}
	public void setFechaSeguimiento(String fechaSeguimiento) {
		this.fechaSeguimiento = fechaSeguimiento;
	}
}
